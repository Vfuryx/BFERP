    /**
     * 审核
     */
    public function audit()
    {
        $this->audit_at = Carbon::now();
        $this->auditor = 'admin';
        $this->is_audit = 1;
        $this->save();
    }


    /**
     * 打印
     */
    public function print()
    {
        $this->is_print = 1;
        $this->save();
    }



    public function purchaseReturnDetails()
    {
        return $this->hasMany(PurchaseReturnDetail::class,'purchase_returns_id');
    }



}
